#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	size_t	i;

	i = 0;
	if (NULL == lst)
		return (i);
	i++;
	while (lst->next != NULL)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
